// --- Layer indicator ---
bool oled_task_user(void) {
    // Left side
    if (is_keyboard_master()) {
        switch (get_highest_layer(layer_state)) {
            case _BASE :
                oled_write_raw_P(base_img, sizeof(base_img));
                break;
            case _GAME:
                oled_write_raw_P(game_img, sizeof(game_img));
                break;
            case _WM :
                oled_write_raw_P(wm_img, sizeof(wm_img));
                break;
            case _MOUSE:
                oled_write_raw_P(mouse_img, sizeof(mouse_img));
                break;
            case _NAV:
                oled_write_raw_P(nav_img, sizeof(nav_img));
                break;
            case _SYM :
                oled_write_raw_P(sym_img, sizeof(sym_img));
                break;
            case _NUM :
                oled_write_raw_P(num_img, sizeof(num_img));
                break;
            case _FUNC :
                oled_write_raw_P(func_img, sizeof(func_img));
                break;
            case _MEDIA:
                oled_write_raw_P(media_img, sizeof(media_img));
                break;
            case _QWERTY:
                oled_write_raw_P(game_img, sizeof(game_img));
                break;
        }

    // Right side
    } else {
        oled_write_raw_P(dreambrace_img, sizeof(dreambrace_img));
    }
    return false;
}

