// --- Custom key declaration ---
enum custom_keycodes {
// These two are used to prevent typos that occur when accidentally chording
// keys instead of switching back to the base layer in common vim commands
    // Key that sends "w" instead of "!" if ":" was recently pressed
    KC_EXCL_W = SAFE_RANGE,
    // Key that sends "q" instead of "~" if ":" was recently pressed
    KC_TILD_Q,
};

// Timer to track colon key press
static uint16_t colon_timer = 0;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        // Track when the colon key is pressed
        case KC_COLON:
            if (record->event.pressed) {
                colon_timer = timer_read();
            }
            return true;

        case KC_EXCL_W:
            if (record->event.pressed) {
                if (timer_elapsed(colon_timer) < 200) {
                    tap_code(KC_W);
                } else {
                    // tap_code16() for "!" to avoid some overflow error
                    tap_code16(S(KC_1));
                }
            }
            return false;

        case KC_TILD_Q:
            if (record->event.pressed) {
                if (timer_elapsed(colon_timer) < 200) {
                    tap_code(KC_Q);
                } else {
                    // tap_code16() for "~" to avoid some overflow error
                    tap_code16(S(KC_GRV));
                }
            }
            return false;

        default:
            return true;
    }
}

