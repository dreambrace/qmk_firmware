// Layer switching
#define LS_NAV LT(_NAV, KC_ESC)
#define LS_SHIFT LSFT_T(KC_ENT)
#define LS_SYM LT(_SYM, KC_BSPC)
#define LS_NUM LT(_NUM, KC_SPC)
#define LS_WM LT(_WM, KC_Z)
#define LS_FUN MO(_FUN)
#define LS_MEDIA LT(_MEDIA, KC_SLSH)

// One-shot mods
