// Layer switching
#define LS_MOUSE LT(_MOUSE, KC_TAB)
#define LS_SHIFT LSFT_T(KC_ENT)
#define LS_NAV LT(_NAV, KC_ESC)
#define LS_SYM LT(_SYM, KC_BSPC)
